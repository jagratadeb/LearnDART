// Null Safety helps you avoid null exceptions in your code.
// It ensures that variables cannot be null unless explicitly declared as nullable.

void main() {
  String? language = null; // Nullable variable
  double? version = 3.8; // Nullable variable
  print("Language: $language");
  print("Version: $version");
}


// Null safety helps protect your app from unexpected crashes that occur when your code tries to use null values in operations.