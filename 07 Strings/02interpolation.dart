// Example for string interpolation in Dart
void main() { 
  String name = "Jagrata Deb";
  int age = 20;

  // Using string interpolation to create a formatted string
  String introduction = "My name is $name and I am $age years old.";
  
  // Printing the result
  print(introduction); // Output: My name is Jagrata Deb and I am 25 years old.
} 