// String properties and methods demonstration
void main() {
  String str = "Hello, Dart!";
  print("Original String: $str");
  print("Length: ${str.length}");
  print("Uppercase: ${str.toUpperCase()}");
  print("Lowercase: ${str.toLowerCase()}");
  print("Contains 'Dart': ${str.contains('Dart')}");
  print("Is the string empty? ${str.isEmpty}");
  print("Is the string not empty? ${str.isNotEmpty}");
}
