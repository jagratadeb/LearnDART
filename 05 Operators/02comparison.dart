// Dart program to demonstrate comparison operators
void main() {
  int a = 10;
  int b = 5;
  int c = 10;

  print('a = $a, b = $b, c = $c');
  print('a == b: ${a == b}'); // Equal to
  print('a == c: ${a == c}'); // Equal to
  print('a != b: ${a != b}'); // Not equal to
  print('a > b: ${a > b}'); // Greater than
  print('b < a: ${b < a}'); // Less than
  print('a >= c: ${a >= c}'); // Greater than or equal to
  print('b <= a: ${b <= a}'); // Less than or equal to
}
